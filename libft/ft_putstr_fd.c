#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	char	*s1;

	if (!s)
		return ;
	s1 = s;
	while (*s1)
	{
		ft_putchar_fd(*s1, fd);
		s1++;
	}
}
