#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*p;

	p = malloc(nmemb * size);
	if (p == NULL)
		return (NULL);
	if (nmemb * size == 0)
		return (p);
	ft_bzero(p, nmemb * size);
	return (p);
}
